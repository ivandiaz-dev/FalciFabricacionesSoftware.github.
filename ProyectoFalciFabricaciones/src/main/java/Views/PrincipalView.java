    private void fecha(){
        LocalDate now = LocalDate.now();
        Locale spanishLocale = new Locale("es", "ES");
        
        fechaLabel.setText(now.format(DateTimeFormatter.ofPattern("'Hoy es' EEEE dd 'de' MMMM 'de' yyyy", spanishLocale)));
    }
    
